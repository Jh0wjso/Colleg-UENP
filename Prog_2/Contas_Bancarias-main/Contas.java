class Contas{
    String nomeTitular;
    int numeroConta;
    int numeroAgencia;
    double saldoConta;

    Contas(String nomeTitular, int numeroConta, int numeroAgencia, double saldoConta){
        this.nomeTitular = nomeTitular;
        this.numeroConta = numeroConta;
        this.numeroAgencia = numeroAgencia;
        this.saldoConta = saldoConta;
    }
}